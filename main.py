def greaterThan(x,y):
    if x > y:
        return True
    else:
        return False

print(greaterThan(10,5))

def lessThan(x,y):
    if x < y:
        return True
    else: 
        return False

print(lessThan(5, 10))

def equalTo(x,y):
    if x == y:
        return True
    else: 
        return False 

print(equalTo(12, 12))

def greaterOrEqual(x,y):
    if x >= y:
        return True
    else:
        return False

print(greaterOrEqual(25, 5))

def lessOrEqual(x,y):
    if x <= y:
        return True
    else: 
        return False

print(lessOrEqual(2, 13))


